package com.triangla.basic2;

public class ExcapeCharacter {
    public static void main(String[] args) {

        System.out.println("Hello \"Obaidur\"");
        System.out.println("Hello \'Obaidur\'");
        System.out.println("Hello \tObaidur");
        System.out.println("Hello \bObaidur");
        System.out.println("Hello \nObaidur");
        System.out.println("Hello \rObaidur");
        System.out.println("Hello \fObaidur");
        System.out.println("Hello \\Obaidur");


    }
}
